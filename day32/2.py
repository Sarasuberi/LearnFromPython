# 断言函数
# 常用的断言函数
# assertEqual(a, b) 断言两个值相等，如果不相等则抛出异常，并输出错误信息
# assertNotEqual(a, b) 断言两个值不相等，如果相等则抛出异常，并输出错误信息
# assertTrue(x) 断言 x 为 True，如果 x 为 False 则抛出异常，并输出错误信息
# assertFalse(x) 断言 x 为 False，如果 x 为 True 则抛出异常，并输出错误信息
# assertIs(a, b) 断言 a 和 b 是同一个对象，如果不是则抛出异常，并输出错误信息
# assertIsNot(a, b) 断言 a 和 b 不是同一个对象，如果是则抛出异常，并输出错误信息
# assertIsNone(x) 断言 x 为 None，如果不是则抛出异常，并输出错误信息
# assertIsNotNone(x) 断言 x 不是 None，如果是则抛出异常，并输出错误信息
# assertIn(a, b) 断言 a 在 b 中，如果不在则抛出异常，并输出错误信息
# assertNotIn(a, b) 断言 a 不在 b 中，如果在则抛出异常，并输出错误信息
# assertIsInstance(a, b) 断言 a 是 b 的一个实例，如果不是则抛出异常，并输出错误信息
# assertNotIsInstance(a, b) 断言 a 不是 b 的一个实例，如果是则抛出异常，并输出错误信息
# assertRaises(a, b) 断言在执行 b 时会抛出 a 类型的异常，如果没有抛出异常则抛出 AssertionError，并输出错误信息
# assertAlmostEqual(a, b) 断言 a 和 b 相近，如果相差超过一定范围则抛出异常，并输出错误信息